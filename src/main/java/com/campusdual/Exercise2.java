package com.campusdual;

public class Exercise2 {
    public static void main(String[] args) {

        //area circulo, longitud circunferencia
        int radius = 15;
        double piNumber = 3.14159;

        double circleArea = (piNumber*(radius*radius));
        double circunferenceLong = 2*piNumber*radius;

        System.out.println("Circle area: "+circleArea);
        System.out.println("Circunference longitude: "+circunferenceLong);

    }
}
